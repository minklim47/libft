#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (n > 0)
	{
		*(unsigned char *)(s + i) = c;
		i++;
		n--;
	}
	return (s);
}
